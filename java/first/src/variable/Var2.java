package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; // 변수선언
        a = 10; // 변수 초기화
        System.out.println(a);
        a = 20;
        System.out.println(a);
        int b = 60; // 선언 및 초기화 같이
        System.out.println(b);
        int c = 3, d = 4; // 여러변수 선언 및 초기화
        System.out.println(c);
        System.out.println(d);
    }
}
