package variable;

public class Var3 {
    public static void main(String[] args) { // psvm
        int a = 100; // 정수
        double b = 10.55; // 실수
        boolean c = true; // 참거짓
        char d = 'a'; // 문자 하나
        String e = "hello"; // 문자열
        System.out.println(a); // sout
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        // 리터럴 = 개발자가 직접 적은 변수를 리터럴 이라 함
        // a 는 정수 리터럴
    }
}
